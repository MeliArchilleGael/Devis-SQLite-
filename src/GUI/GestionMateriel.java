    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int choix = JOptionPane.showConfirmDialog(null,"Voulez vous vraiment Supprimer cette ligne? ","Supprimer",JOptionPane.WARNING_MESSAGE);
        
        if (choix == JOptionPane.YES_OPTION){
            int s = this.jTable1.getSelectedRow();
           
            if(s != -1){
                DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();
                int num = Integer.parseInt(model.getValueAt(s,0).toString());
                
                if(new Material().Delete(num) == 1 ){                   
                    JOptionPane.showMessageDialog(null,"Supprimer avec succes ","Supprimer",JOptionPane.INFORMATION_MESSAGE);
                    model.removeRow(s);
                }
                
                
            }else {
                JOptionPane.showMessageDialog(null,"Veillez selectionner une ligne du tableau","Erreur : #007",JOptionPane.WARNING_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:
        //JOptionPane.showMessageDialog(null,"Désolé cette option n'est pas disponible sur cette version","Erreur: #009",JOptionPane.OK_OPTION);
        int s = this.jTable1.getSelectedRow();
           
            if(s != -1){
               DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();
               
               //create a new material
               Material mat = new Material();
               mat.setDesignation(model.getValueAt(s,1).toString());
               mat.setId(Integer.parseInt(model.getValueAt(s,0).toString()));
               mat.setUnitPrice(Integer.parseInt(model.getValueAt(s,2).toString()));
               
               new NewMaterial(mat).setVisible(true);
               this.dispose();
            }
                
                
                
    }//GEN-LAST:event_jButtonUpdateActionPerformed


     private void setjTable1() {
         DefaultTableModel dt = (DefaultTableModel) this.jTable1.getModel();
         dt.setRowCount(0);
         this.material.stream().forEach((Material a)->{
             Object [] ob = {a.getId(), a.getDesignation(), a.getUnitPrice()};
             
             dt.addRow(ob);
         });
    }
    
